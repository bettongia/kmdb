  static final Map<String, int> _kWordToIndex = {
    for (var i = 0; i < _kWordlist.length; i++) _kWordlist[i]: i,
  };

  /// Encodes 16 bytes of [entropy] as a 16-word recovery code string.
  ///
  /// [entropy] must be exactly 16 bytes (128 bits).
  /// Returns a space-separated string of 16 lowercase words.
  static String encode(Uint8List entropy) {
    if (entropy.length != 16) {
      throw ArgumentError.value(
        entropy.length,
        'entropy.length',
        'Recovery entropy must be exactly 16 bytes',
      );
    }
    return entropy.map((b) => _kWordlist[b]).join(' ');
  }

  /// Decodes a [code] string back to 16 bytes of entropy.
  ///
  /// [code] must be exactly 16 space-separated words from [_kWordlist].
  ///
  /// Throws [FormatException] if the code is invalid (wrong number of words,
  /// or a word not in the vocabulary).
  static Uint8List decode(String code) {
    final words = code.trim().split(RegExp(r'\s+'));
    if (words.length != 16) {
      throw FormatException(
        'Recovery code must contain exactly 16 words, got ${words.length}',
        code,
      );
    }
    final bytes = Uint8List(16);
    for (var i = 0; i < 16; i++) {
      final word = words[i].toLowerCase();
      final index = _kWordToIndex[word];
      if (index == null) {
        throw FormatException(
          'Unknown word in recovery code at position $i: "$word"',
          code,
        );
      }
      bytes[i] = index;
    }
    return bytes;
  }
