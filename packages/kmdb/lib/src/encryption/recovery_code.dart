/// vocabulary (`_kWordlist`). The result is a space-separated 16-word string.
///
/// ## Rationale for a 256-word list
///
/// A 256-word list allows each word to carry exactly 8 bits, keeping
/// the encoding trivial (no bit-shifting, direct index lookup) while producing
/// a memorable, unambiguous code. The words are chosen to be short (3–5
/// letters), distinct, and pronounceable to minimise transcription errors.
///
